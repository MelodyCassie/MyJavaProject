public class ArrayOfCharactersTwo {
    public static void main(String[] args) {
        String[] characters = new String[7];

        characters[0] = "A";
        characters[1] = "M";
        characters[2] = "C";
        characters[3] = "W";
        characters[4] = "I";
        characters[5] = "T";

        System.out.println(characters[0] + characters[3] + characters[1] + characters[4] + characters[2] + characters[5]);
    }
}
