package PythonToJava;

public class ForLoopShape {
    public static void main(String[] args) {
int index = 0;
        for(int count = 0 ; count <= 4 ; count++){
            for(index = 0 ; index <= count ; index++)
                System.out.print("*  ");
        }
        System.out.print(" ");

    }
}
