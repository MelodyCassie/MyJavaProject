package tdd;

public class Human {
    private String name;
    private int age;
    private String phoneNumber;



    public void setName(String name) {
        this.name = name;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public void setPhoneNumber(String number) {
        phoneNumber = number; }



    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public String getPhoneNumber() {
        return phoneNumber;
    }


}